public class questao14 {
    public static void main(String[] args) {
        // Faça um programa que exiba os números ímpares de 1 a 100 utilizando o while.
        int i = 0;
        while (i <= 100){
            if (i%2 != 0){
                System.out.println(i);
            }
            i++;
        }
    }
}